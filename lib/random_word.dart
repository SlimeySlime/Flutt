class RandomWord extends StatefulWidget {
  @override
  RandomWordState createState() {
    return RandomWordState();
  }
}

class RandomWordState extends State<RandomWord> {
  final _wordList = <WordPair>[];
  final _biggerFont = const TextStyle(fontSize: 18.0);

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text("new rng"),
      ),
      body: _buildWordList(),
    );
    // return _buildWordList();
  }

  Widget _buildWordList() {
    return ListView.builder(
        padding: const EdgeInsets.all(16.0),
        itemBuilder: /*1*/ (context, i) {
          if (i.isOdd) return Divider(); /*2*/

          final index = i ~/ 2; /*3*/
          if (index >= _wordList.length) {
            _wordList.addAll(generateWordPairs().take(10)); /*4*/
          }
          return _buildRow(_wordList[index]);
        });
  }

  Widget _buildRow(WordPair pair) {
    return ListTile(
      title: Text(
        pair.asPascalCase,
        style: _biggerFont,
      ),
    );
  }
}

class MyHomePage extends StatefulWidget {
  const MyHomePage({super.key, required this.title});

  // This class is the configuration for the state. It holds the values (in this
  // case the title) provided by the parent (in this case the App widget) and
